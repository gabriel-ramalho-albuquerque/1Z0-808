package academy.learnprogramming.a_java_basics;

public class Q_JavaBenefits {
    /*
        Key Java benefits:

            - Object oriented language: All code is defined in classes and most of these classes can be instantiated
                                        into objects (Java allows functional programing within a class,
                                        but object-oriented is still the main organization of code)

            - Encapsulation: Support access modifiers to protect data from unintended access and modification

            - Platform independent: It gets compiled into byte code. Benefit is that Java code gets compiled once rather
                                    than needing to recompile for different operating systems, this is also known as
                                    write once run anywhere.

            - Robust: This is the major advantage over C++, Java prevents memory leaks, it manages memory on its own and
                      does garbage collection automatically. Bad memory management in C++ is a big source of errors in
                      programs.

            - Simple: Java is intended to be simpler than C++ in addition to eliminating pointers and Java doesn't allow
                      operator overloading.

            - Secure: Java code runs inside Java virtual machine also known as JVM. This creates sandbox that makes it
                      hard for Java code to do any damage to the computer that it is running on.
     */
}
